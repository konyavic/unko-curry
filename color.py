COLOR_KEYS = COLORS.keys()
COLOR_KEYS.sort()

def get_color(key):
    head = 0
    tail = len(COLOR_KEYS)
    while head < tail:
        mid = (head + tail) / 2
        v = COLOR_KEYS[mid]
        if key > v:
            head = mid + 1
        elif key < v:
            tail = mid - 1
        else:
            head = mid
            break

    return COLORS[COLOR_KEYS[head]]
